    public void Ingresar(){ //Ingresar proceso a la tabla
    DefaultTableModel modelo=(DefaultTableModel) miTabla.getModel();

    Contador ++;
    Object[] tabla = new Object[6];
    tabla[0]= Contador;
    tabla[1]= NOM.getText();
    tabla[2]= prioridad.getText();
    tabla[3]= tarribo.getText();
    tabla[4]=tam.getText();
    tabla[5]=duracion.getText();
    //tabla[6]= "Listo";
    modelo.addRow(tabla);
    //miTabla.setModel(modelo);
    NOM.setText(null);
    NOM.grabFocus();
    prioridad.setText(null);
    prioridad.grabFocus();
    tarribo.setText(null);
    tarribo.grabFocus();
    tam.setText(null);
    tam.grabFocus();
    duracion.setText(null);
    duracion.grabFocus();
}
